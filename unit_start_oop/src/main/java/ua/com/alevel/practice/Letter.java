package ua.com.alevel.practice;

public abstract class Letter {

    private int sf;
    private String aljed;

    public int getSf() {
        return sf;
    }

    public void setSf(int sf) {
        this.sf = sf;
    }

    public String getAljed() {
        return aljed;
    }

    public void setAljed(String aljed) {
        this.aljed = aljed;
    }
}
